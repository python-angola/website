const buttons = Array.from(document.querySelectorAll('.lang-btn'));
const heroDynamicText = document.getElementById('hero-dynamic-text');
let dynamicIndex = 0;
let currentLanguage = 'en';

function getPreferredLanguage() {
  const language = navigator.language || navigator.userLanguage || 'en';
  const code = language.toLowerCase();

  if (code.startsWith('pt')) return 'pt';
  if (code.startsWith('fr')) return 'fr';
  if (code.startsWith('en')) return 'en';

  const found = (navigator.languages || []).find((lang) => {
    const normalized = lang.toLowerCase();
    return normalized.startsWith('pt') || normalized.startsWith('fr') || normalized.startsWith('en');
  });

  if (found) {
    if (found.startsWith('pt')) return 'pt';
    if (found.startsWith('fr')) return 'fr';
  }

  return 'en';
}

function setActiveLanguageButton(code) {
  buttons.forEach((button) => {
    button.classList.toggle('is-active', button.dataset.lang === code);
  });
}

function updateTexts(lang) {
  const dictionary = translations[lang] || translations.en;
  Object.entries(textMap).forEach(([key, selector]) => {
    const element = document.querySelector(selector);
    if (!element) return;
    element.textContent = dictionary[key];
  });
  currentLanguage = lang;
  setActiveLanguageButton(lang);
}

function rotateHeroText() {
  const phrases = dynamicPhrases[currentLanguage] || dynamicPhrases.en;
  heroDynamicText.textContent = phrases[dynamicIndex % phrases.length];
  dynamicIndex += 1;
}

buttons.forEach((button) => {
  button.addEventListener('click', () => {
    const lang = button.dataset.lang;
    updateTexts(lang);
    rotateHeroText();
  });
});

function initializeSite() {
  const preferred = getPreferredLanguage();
  updateTexts(preferred);
  rotateHeroText();
  setInterval(rotateHeroText, 4200);
}

initializeSite();
